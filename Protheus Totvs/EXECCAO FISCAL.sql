FROM SB1010 B1 WITH(NOLOCK)
INNER JOIN SD1010 D1  WITH(NOLOCK) ON D1_COD = B1_COD AND D1.D_E_L_E_T_=''
INNER JOIN SF1010 F1  WITH(NOLOCK) ON F1_FORNECE = D1_FORNECE AND F1_SERIE <> '9' AND F1_LOJA = D1_LOJA AND F1.D_E_L_E_T_='' AND D1_SERIE = F1_SERIE AND F1_EST <> 'SP'
INNER JOIN SF7010 F7  WITH(NOLOCK) ON F7_GRTRIB = B1_GRTRIB AND F7_EST = F1_EST AND F7.D_E_L_E_T_ = ''
INNER JOIN SF4010 F4  WITH(NOLOCK) ON F4_CODIGO = D1_TES AND F4_OPEMOV ='01' AND F4.D_E_L_E_T_ = ''

WHERE (B1_TE IN('003','017') OR D1_TES IN('003','017')) AND B1_UCOM >= '2020%' 
ORDER BY 1













