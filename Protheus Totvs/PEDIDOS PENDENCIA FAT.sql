--ANALISA PEDIDOS PENDENTES DE FATURAMENTO QUE NÃO FORAM ABERTOS.

SELECT DISTINCT C6_FILIAL,C6_NUM 
--UPDATE SC5010 SET C5_NOTA = '', C5_SERIE = '', C5_LIBEROK =''
FROM SC5010 C6 
INNER JOIN SC6010 C5 ON C5_NUM = C6_NUM AND C5_FILIAL = C5_FILIAL AND  C5_NOTA != '' AND C5_SERIE != '' AND C5_LIBEROK !='' AND C5.D_E_L_E_T_=''
WHERE C6_QTDVEN-C6_QTDENT > 0 AND C6_QTDENT<>0  AND C6_BLQ ='' AND C6.D_E_L_E_T_ = '' AND C6_CLI NOT IN ('004771','000268')

ORDER BY 1,2