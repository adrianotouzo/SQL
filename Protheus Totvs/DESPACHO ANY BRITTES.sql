SELECT Z02_IDWEB IDWEB, Z02_PEDIDO PEDIDO , ZRL_NUMDOC 
FROM Z02010 PED (NOLOCK) 
JOIN ZRL010 ZRL (NOLOCK) ON ZRL_NUMDOC = Z02_DOC
WHERE PED.Z02_FILIAL = '01' 	
AND PED.D_E_L_E_T_ = ''  	
AND PED.Z02_DTPGTO >= '20210421'

SELECT Z02_IDWEB IDWEB, Z02_PEDIDO PEDIDO , ZRL_NUMDOC 
FROM Z02010 PED (NOLOCK) 
JOIN ZRL010 ZRL (NOLOCK) ON ZRL_NUMDOC = Z02_DOC AND ZRL_ACAO = 'DESPACHO NFE'
WHERE PED.Z02_FILIAL = '01' 	
AND PED.D_E_L_E_T_ = ''  	
AND PED.Z02_DTPGTO >= '20210421'