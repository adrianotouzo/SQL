-- VIEW OCORRENCIA
CREATE VIEW  OCORRENCIA  AS 
SELECT U9_CODIGO   AS COD_OCORRENCIA
     , U9_DESC     AS DES_OCORRENCIA
  FROM OXICLOUD23.dbo.SU9010
 WHERE U9_VALIDO ='1' 
   AND U9_TIPOATE IN ('2','4') 
   AND D_E_L_E_T_ = '';

