FROM OXICLOUD23.dbo.ZZ9010 WHERE  D_E_L_E_T_ = ''



-- VIEW PAIS 
ALTER VIEW  PAIS  AS 
SELECT DISTINCT X5_TABELA, X5_CHAVE, X5_DESCRI, X5_DESCSPA, X5_DESCENG
FROM OXICLOUD23.dbo.SX5010 WHERE X5_TABELA = 'HC'
AND D_E_L_E_T_ = ''

SELECT * FROM PAIS

