--MEDIA DE CONSUMO DE PODUTO

SELECT SUBSTRING(D2_EMISSAO,1,6) CDATA ,
SUM(CASE WHEN D2_FILIAL = '01' THEN D2_QUANT ELSE 0 END)ARA,
SUM(CASE WHEN D2_FILIAL = '02' THEN D2_QUANT ELSE 0 END)SCA
FROM SD2010 D2
INNER JOIN SF4010 F4 ON D2_TES = F4_CODIGO AND F4_DUPLIC = 'S' AND F4.D_E_L_E_T_ <> '*' 
WHERE D2_TIPO NOT IN ('D','B')  AND  D2_TES <> '571' AND  D2.D_E_L_E_T_ <> '*' 
AND SUBSTRING(D2_EMISSAO,1,6) BETWEEN '202108' AND '202108'
AND D2_COD = '020150039'
GROUP BY SUBSTRING(D2_EMISSAO,1,6)
ORDER BY SUBSTRING(D2_EMISSAO,1,6)



SELECT CONVERT(VARCHAR,F2_EMISSAO, COUNT(*) AS TOTAL
FROM SF2010 WHERE F2_EMISSAO BETWEEN '20210101' AND '20210831'
GROUP BY F2_EMISSAO

